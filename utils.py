def normalize_for_match(text):
    """
    Нормализует текст для фаззи-поиска: 
    1. Приводит к нижнему регистру.
    2. Убирает все, кроме букв и цифр (очищает от знаков препинания).
    3. Транслитерирует кириллицу в латиницу (для поиска).
    4. Удаляет повторяющиеся пробелы и пробелы в начале/конце.
    """
    if not text:
        return ""
    
    # 1. Приведение к нижнему регистру
    text = text.lower()
    
    # 2. Транслитерация кириллицы в латиницу (unidecode отлично справляется, но можно добавить ручную замену)
    text = unidecode(text)
    
    # 3. Удаление всех символов, кроме букв, цифр и пробелов
    text = re.sub(r'[^a-z0-9\s]', '', text)
    
    # 4. Удаление лишних пробелов
    text = re.sub(r'\s+', ' ', text).strip()
    
    return text